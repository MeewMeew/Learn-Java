package Bai_3;

public class Hinh {
  private String mau;
  private int soCanh;

  public Hinh() {
    this.mau = "Red";
    this.soCanh = 4;
  }

  public String getMau() {
    return mau;
  }

  public void setMau(String mau) {
    this.mau = mau;
  }

  public int getSoCanh() {
    return soCanh;
  }

  public void setSoCanh(int soCanh) {
    this.soCanh = soCanh;
  }
}
